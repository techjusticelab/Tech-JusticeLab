			<div class="rounded-lg bg-white p-8 shadow-md">
				<h3 class="mb-4 text-2xl font-semibold text-[var(--cerulean-blue)]">
					Racial Justice Act Tool
				</h3>
				<p class="mb-6 text-gray-700">
					The Berkeley Technology and Justice Lab, in partnership with the Berkeley Criminal Law and
					Justice Center, has created a textual analysis tool that identifies instances of
					racialized language per claims A1 and A2 of the California Racial Justice Act. In its
					current stage the tool is primarily for use by public defenders and is designed using
					training data from each county to be able to identify the racial nuances of the given
					geographic area. The tool is set to be piloted with the Los Angeles County Public
					Defender's Office, after which it can be expanded to PD offices throughout the state. Long
					term they hope to collaborate with other organizations creating RJA tools to build an
					end-to-end tool for use by defense attorneys, family/loved ones of incarcerated people,
					and the general public to access digital tools for each step in the RJA filing process.
				</p>
				<div class="flex items-center">
					<button class="btn-primary rounded-md px-6 py-2 text-sm font-medium">View Demo</button>
				</div>
			</div>

